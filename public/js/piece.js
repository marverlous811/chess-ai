const reverseArray = function(array) {
    return array.slice().reverse();
};

function getAbsolutedValue(type, isWhite, x, y) {
    // console.log("piece type ", type)
    if (!CHESS_VALUE.hasOwnProperty(type))
        return 0

    if(!CHESS_POSITION_VALUE.hasOwnProperty(type))
        return 0
        
    const positionArr = isWhite ? CHESS_POSITION_VALUE[type] : reverseArray(CHESS_POSITION_VALUE[type])

    // console.log("piece value ", CHESS_VALUE[type])
    return CHESS_VALUE[type] + positionArr[x][y]
}

function getPieceValue(piece, x, y){
    if(piece == null){
        return 0;
    }
    const absValue = getAbsolutedValue(piece.type, piece.color === 'w', x, y)
    // console.log("piece color ", piece.color)
    return piece.color === 'w' ? absValue : -absValue
}

function evaluateBoard(board){
    let total = 0;
    for (let i = 0; i < 8; i++){
        for (let j = 0; j < 8; j++ ) {
            total += getPieceValue(board[i][j], i, j)
        }
    }

    return total
}